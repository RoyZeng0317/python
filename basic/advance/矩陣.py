import numpy as np

# ===================================================
# Python 矩陣教學：使用 NumPy 處理數學矩陣運算
# ===================================================

# 1. 建立矩陣
# ---------------------------------------------------
A = np.array([[1, 2],
              [3, 4]])
B = np.array([[5, 6],
              [7, 8]])

print("矩陣 A:\n", A)
print("矩陣 B:\n", B)

# 2. 加法與減法（維度要相同，對應位置相加減）
# ---------------------------------------------------
print("\nA + B =\n", A + B)
print("A - B =\n", A - B)

# 3. 純量乘法（每個元素都乘上同一個數字）
# ---------------------------------------------------
print("\n3 * A =\n", 3 * A)

# 4. 逐元素相乘 vs 矩陣乘法（最常搞混的觀念）
# ---------------------------------------------------
print("\nA * B（逐元素相乘，不是數學上的矩陣乘法）=\n", A * B)
print("A @ B（矩陣乘法，等同 np.dot(A, B)）=\n", A @ B)

# 5. 轉置矩陣（列變欄、欄變列）
# ---------------------------------------------------
print("\nA 的轉置 A.T =\n", A.T)

# 6. 單位矩陣（對角線為 1，其餘為 0；乘上任何矩陣都不變）
# ---------------------------------------------------
I = np.eye(2)
print("\n單位矩陣 I =\n", I)
print("A @ I =\n", A @ I)

# 7. 行列式 det(A)：判斷矩陣是否可逆（det = 0 代表不可逆）
# ---------------------------------------------------
det_A = np.linalg.det(A)
print("\ndet(A) =", det_A)

# 8. 反矩陣 A^-1：A @ A^-1 會得到單位矩陣
# ---------------------------------------------------
if det_A != 0:
    A_inv = np.linalg.inv(A)
    print("A^-1 =\n", A_inv)
    print("A @ A^-1 =\n", A @ A_inv)
else:
    print("A 沒有反矩陣（行列式為 0）")

# 9. 解聯立方程式 Ax = b
#    等同於：x + 2y = 5
#           3x + 4y = 6
# ---------------------------------------------------
b = np.array([5, 6])
x = np.linalg.solve(A, b)
print("\nAx = b 的解 x =", x)

# 10. 特徵值與特徵向量（進階，選讀）
# ---------------------------------------------------
eigenvalues, eigenvectors = np.linalg.eig(A)
print("\n特徵值:", eigenvalues)
print("特徵向量:\n", eigenvectors)
